<h5>25.12.13</h5><h1>The Bandwidth of Experience</h1><p>I wrote this on christmas eve. The lady and I had ingested a metric fuckton of fondue and white wine, and proceeded to sleepily watch the jungle book. these were my wearly thoughts afterwards.</p>

<p>sound has such a profound connection<br />
  perhaps its own connection, as with music<br />
read words can eventually equate to (inspire) ideas, but only ideas<br />
  pictures, too<br />
and video is as close to memory as we can get</p>

<p>thus, errors in sound are easily noticeable<br />
whereas errors in words and images are easily forgivable<br />
  our brains make the connection for us instead?<br />
errors in video are EGREGIOUS</p>

<p>the bandwidth of experience.</p>

<p>isn't it interesting that an image is so small, while its sound recording could be many thousands of times larger; its video counterpart, larger still?</p>

<p>(when we finally quantify smell, will be even larger)</p>

<p>perhaps is why we're trying to produce the most 'essential' experience in the limited bandwidth we have to work with, e.g. buzzfeed.</p>

<p>perhaps we have reached the outer limits of what we're able to do with the given bandwidth</p>

<p>// only when we're able to transmit actual, quantifiable feeling, will communication be complete</p>

<p>until then, we're simply competing in the limited sensory space that we have control over<br />
  some senses &amp;&amp; their mediums have more advantages than others</p>

<p>(still a lot of fun to be had, though!)</p>

<p>and until someone is able to conquer one of the remaining senses, we will not see innovation on the scale we expect<br />
  iPad / iPhone captured touch &amp; tactile<br />
  glass tried to bridge gap with verbal feedback, ineffectively</p>

<p>  retina is nice, but doesn't really ADD anything<br />
  5.1, 7.1, 9.1, 91.1 too<br />
    simply overkill<br />
  megapixels?!</p>

<p>THIS IS WHY WE AS DESIGNERS MUST WORK SO HARD TO CAPTURE OUR AUDIENCES.<br />
with so much noise in the world<br />
with so much competing for everyone else's attention<br />
  not even their empathy, but their fucking attention<br />
we have to bring our A game every last day<br />
bandwidth is cheap<br />
and its only getting cheaper.<br />
every further advancement in science and human interaction is ultimately another layer against which the creative must compete</p>

<p>adversely, its another layer in which we can play!</p>
